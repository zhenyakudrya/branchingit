def upp_func(value):
    """
    Делает заглавными все буквы
    """
    new_value = value.upper()
    return new_value

def tittle_func(value):
    """
    Делает заглавными первые буквы
    каждого слова в строке,
    поступившей на вход функции
    """
    new_value = value.tittle()
    return new_value